#include "header.h"

int		ft_add(int a, int b)
{
	return (a + b);
}

int		ft_soustract(int a, int b)
{
	return (a - b);
}

int		ft_divide(int a, int b)
{
	return (a / b);
}

int		ft_modulo(int a, int b)
{
	return (a % b);
}

int		ft_mult(int a, int b)
{
	return (a * b);
}
