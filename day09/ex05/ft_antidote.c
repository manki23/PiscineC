int		ft_antidote(int i, int j, int k)
{
	if ((i < j && j < k) || (i > j && j > k))
		return (j);
	else if ((j < i && i < k) || (j > i && i > k))
		return (i);
	else if ((i < k && k < j) || (i > k && k > j))
		return (k);
	return (-1);
}
