int		ft_max(int *tab, int length)
{
	int		i;
	int		j;
	int		swap;

	j = length;
	while (j != 0)
	{
		i = 0;
		while ((i + 1) < j)
		{
			if (tab[i] > tab[i + 1])
			{
				swap = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = swap;
			}
			i++;
		}
		j--;
	}
	return (tab[length - 1]);
}
